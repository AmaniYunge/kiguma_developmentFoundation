        <strong>Introduction:</strong>

        <p>This Accounting Manual shall facilitate the decision- making and or implementation of financial dealing of KDF.

        It provide guidelines to employees on all financial operations reporting and preparation of financial statements.  It is also a tool for the practicing accountant to meet the accounting procedures as required by both the Management of KDF and other agencies including donors of the projects concerned in regards to the approved accounting principles.

