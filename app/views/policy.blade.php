        <h5>ATTACHMENTS IN THE FINANCIAL STATEMENTS</h5>
        <strong>Monthly Financial Report (MFR)</strong>
        <p>This is the form prepared at the end of every month. It shows the full picture of the financial situation.</p>

        <strong>It has five columns and the summary to date table:</strong>
        <li>Column 1:  This shows the budget line items include/constructing the budget.</li>
        <li>Column  2 :  This shows the budget amount approved in every line item of the budget.</li>
        <li>Column  3 :   This shows the amount spent in a month of the report.</li>
        <li>  Column  4 :    This shown the cumulative expenditure from the begining up to the date of report.</li>
        <li>Column  5 :    This shows the remaining budget balance, it is column  2 minus column  4</li>

       <strong> Total = This shows totals of every column.</strong><br>


        <strong>Summary to date:</strong>
        <li>Previously received  - This shows the amount received previously.</li>
        <li>Received this month  -  This shows the amount received during the month of the report.</li>
        <li>Total received   -  Is the sum of previously received and received this month.</li>
        <li>Total received     -  Is the total of column 4</li>
        <li>Balance on hand   -   Is the total received minus total amount expended.</li>
        <li>Approval   authorized   -   This is a space to be signed by the organisation officer.</li><br>

        <h5>CASH BOOK SUMMARY:</h5>

        <p>This is a cash book summary for the report month,the balance of this should agree with that of the MFR(Balance on hand).</p>

        <h5>BANK RECONCILLIATION  STATEMENT:</h5>

        <p>This should be done in a monthly basis, and any different if found should be worked out timely.</p>

       <h5>PIPELINE   ANALYSIS:</h5>

        <strong>This shows the budget burn rate in percentage(%). It has columns:</strong>
        <li>Column  1          -    It shows the budget line items.</li>
        <li>Column   2         -    It shows the life of Project (LPO) Budget</li>
        <li>Column   3         -    Amount expended this month</li>
        <li>Column   4         -    Amount expended to date</li>
        <li>Column   5         -    Percentage of Budget burn rate ( Column 4 divided by column 2)</li>
        <li>Column   6         -    Life of Project Budget balance(LPO)
            ( Column 2 minus column 4 )</li>
        <li>Column   7         -     Percentage of Life of Project Budget Balance(Column  6 divided by
            column 2)</li>


